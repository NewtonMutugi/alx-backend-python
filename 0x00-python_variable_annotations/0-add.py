#!/usr/bin/python3
""" type-annotated function add """


def add(a: float, b: float) -> float:
    """
    takes a float a and a float b as arguments and returns their sum as a float

    Args:
        a (float): first float
        b (float): _second float_

    Returns:
        float: _sum as a float.

    """
    return a + b
